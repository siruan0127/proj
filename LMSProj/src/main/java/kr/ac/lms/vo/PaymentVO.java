package kr.ac.lms.vo;

import java.util.Date;

import org.apache.commons.lang.builder.ToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;

public class PaymentVO {
	private int payCd;
	private int stuCd;
	private int paySem;
	private String payDt;
	private int payAmt;
	private int payYn;
	public int getPayCd() {
		return payCd;
	}
	public void setPayCd(int payCd) {
		this.payCd = payCd;
	}
	public int getStuCd() {
		return stuCd;
	}
	public void setStuCd(int stuCd) {
		this.stuCd = stuCd;
	}
	public int getPaySem() {
		return paySem;
	}
	public void setPaySem(int paySem) {
		this.paySem = paySem;
	}
	public String getPayDt() {
		return payDt;
	}
	public void setPayDt(String payDt) {
		this.payDt = payDt;
	}
	public int getPayAmt() {
		return payAmt;
	}
	public void setPayAmt(int payAmt) {
		this.payAmt = payAmt;
	}
	public int getPayYn() {
		return payYn;
	}
	public void setPayYn(int payYn) {
		this.payYn = payYn;
	}

	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.DEFAULT_STYLE);
	}
}
