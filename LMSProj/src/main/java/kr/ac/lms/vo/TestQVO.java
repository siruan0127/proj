package kr.ac.lms.vo;

import org.apache.commons.lang.builder.ToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;

public class TestQVO {
	private int teqCd;
	private int testCd;
	private String teqNo;
	private String teqOp1;
	private String teqOp2;
	private String teqOp3;
	private String teqOp4;
	private int teqAnswer;
	private int rnum;
	
	public int getRnum() {
		return rnum;
	}
	public void setRnum(int rnum) {
		this.rnum = rnum;
	}
	
	public int getTeqCd() {
		return teqCd;
	}
	public void setTeqCd(int teqCd) {
		this.teqCd = teqCd;
	}
	public int getTestCd() {
		return testCd;
	}
	public void setTestCd(int testCd) {
		this.testCd = testCd;
	}
	public String getTeqNo() {
		return teqNo;
	}
	public void setTeqNo(String teqNo) {
		this.teqNo = teqNo;
	}
	public String getTeqOp1() {
		return teqOp1;
	}
	public void setTeqOp1(String teqOp1) {
		this.teqOp1 = teqOp1;
	}
	public String getTeqOp2() {
		return teqOp2;
	}
	public void setTeqOp2(String teqOp2) {
		this.teqOp2 = teqOp2;
	}
	public String getTeqOp3() {
		return teqOp3;
	}
	public void setTeqOp3(String teqOp3) {
		this.teqOp3 = teqOp3;
	}
	public String getTeqOp4() {
		return teqOp4;
	}
	public void setTeqOp4(String teqOp4) {
		this.teqOp4 = teqOp4;
	}
	public int getTeqAnswer() {
		return teqAnswer;
	}
	public void setTeqAnswer(int teqAnswer) {
		this.teqAnswer = teqAnswer;
	}

	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.DEFAULT_STYLE);
	}
}
