package kr.ac.lms.vo;

import org.apache.commons.lang.builder.ToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;

public class NoticeFileVO {
	private int ntcfCd;
	private int ntcCd;
	private String ntcfNm;
	private String ntcfPt;
	private int attachCd;
	
	private AttachVO attachVO;

	public int getNtcfCd() {
		return ntcfCd;
	}

	public void setNtcfCd(int ntcfCd) {
		this.ntcfCd = ntcfCd;
	}

	public int getNtcCd() {
		return ntcCd;
	}

	public void setNtcCd(int ntcCd) {
		this.ntcCd = ntcCd;
	}

	public String getNtcfNm() {
		return ntcfNm;
	}

	public void setNtcfNm(String ntcfNm) {
		this.ntcfNm = ntcfNm;
	}

	public String getNtcfPt() {
		return ntcfPt;
	}

	public void setNtcfPt(String ntcfPt) {
		this.ntcfPt = ntcfPt;
	}

	public int getAttachCd() {
		return attachCd;
	}

	public void setAttachCd(int attachCd) {
		this.attachCd = attachCd;
	}

	public AttachVO getAttachVO() {
		return attachVO;
	}

	public void setAttachVO(AttachVO attachVO) {
		this.attachVO = attachVO;
	}


	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.DEFAULT_STYLE);
	}
}
