package kr.ac.lms.vo;

import java.util.Date;

import org.apache.commons.lang.builder.ToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;

public class AllocationVO {

	private int roomCd;
	private int lecCd;
	private String altDay;
	private Date altSt;
	private Date altEn;
	
	public int getRoomCd() {
		return roomCd;
	}
	public void setRoomCd(int roomCd) {
		this.roomCd = roomCd;
	}
	public int getLecCd() {
		return lecCd;
	}
	public void setLecCd(int lecCd) {
		this.lecCd = lecCd;
	}
	public String getAltDay() {
		return altDay;
	}
	public void setAltDay(String altDay) {
		this.altDay = altDay;
	}
	public Date getAltSt() {
		return altSt;
	}
	public void setAltSt(Date altSt) {
		this.altSt = altSt;
	}
	public Date getAltEn() {
		return altEn;
	}
	public void setAltEn(Date altEn) {
		this.altEn = altEn;
	}

	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.DEFAULT_STYLE);
	}
	
}
